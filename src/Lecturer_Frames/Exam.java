    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            con=connect();
            DefaultTableModel d=(DefaultTableModel)jTable1.getModel();
            int selectIndex = jTable1.getSelectedRow();
            
            String exam_id=txtID.getText();
            String Subject=txtSubject.getText();
            String Student_ID=txtStudent_ID.getText();
            String Question_ID=txtQuestion_ID.getText();
            String Degree=txtDegree.getText();
            txtID.setText(d.getValueAt(selectIndex, 0).toString());
            pst = con.prepareStatement("update exam_table set subject=?,Student_ID=?,Question_ID=?,degree=? where exam_id=? ");
            
            pst.setString(1, Subject);
            pst.setString(2, Student_ID);
            pst.setString(3, Question_ID);
            pst.setString(4, Degree);
            pst.setString(5, exam_id);
            int k = pst.executeUpdate();
            if (k==1) {
                JOptionPane.showMessageDialog(this, "Updated");
                resetPanalDeta();
                jButton1.setEnabled(true);
                Load();
                txtID.requestFocus();
            }else{
                JOptionPane.showMessageDialog(this, "not Updated");
            }
            pst.close();
            con.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

