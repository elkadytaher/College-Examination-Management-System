    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            con=connect();
            String exam_id=txtID.getText();
            String Subject=txtSubject.getText();
            String Student_ID=txtStudent_ID.getText();
            String Question_ID=txtQuestion_ID.getText();
            String Degree=txtDegree.getText();
            pst = con.prepareStatement("insert into exam_table(exam_id,subject,Student_ID,Question_ID,degree) values(?,?,?,?,?)");
            pst.setString(1, exam_id);
            pst.setString(2, Subject);
            pst.setString(3, Student_ID);
            pst.setString(4, Question_ID);
            pst.setString(5, Degree);
            int k = pst.executeUpdate();
            if (k==1) {
                JOptionPane.showMessageDialog(this, "Added");
                resetPanalDeta();
                Load();
                txtID.requestFocus();
                
            }else{
                JOptionPane.showMessageDialog(this, "not added");
            }
            pst.close();
            //con.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

